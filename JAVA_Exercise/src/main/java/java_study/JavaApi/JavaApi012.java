package java_study.JavaApi;

public class JavaApi012 {
    public static void main(String[] args) {
        System.out.println("计算绝对值的结果:"+Math.abs(-1));
        System.out.println("求大于参数的最小整数:"+Math.ceil(7.8));
        System.out.println("求小于参数的最大整数:"+Math.floor(-4.2));
        System.out.println("对小数进行四舍五入后的结果:"+Math.round(-4.6));
        System.out.println("求两个数的较大值:"+Math.max(1,2.2));
        System.out.println("求两个数的较小值:"+Math.min(1,2.2));
        System.out.println("生成一个0和1之间的随机数"+Math.random());
    }
}
